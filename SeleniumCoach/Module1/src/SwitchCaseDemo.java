
public class SwitchCaseDemo {

	public static void main(String[] args) {
		char c = 'e';
		
		switch(c){
			case 'a':
				System.out.println(c+" is Vowel");
				break;
			case 'e':
				System.out.println(c+" is Vowel");
				break;
			case 'i':
				System.out.println(c+" is Vowel");
				break;
			case 'o':
				System.out.println(c+" is Vowel");
				break;
			case 'u':
				System.out.println(c+" is Vowel");
				break;
			default:
				System.out.println(c+" is Consonant");
		}		
	}
}
